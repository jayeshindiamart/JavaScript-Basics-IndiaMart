var array = [1, 2, 3]

// Push Method - Adds the value at the end of the array
array.push(4)
array.push(5)
array.push(6)

// Unshift - Adds the value at the start of an array

array.unshift(0)
array.unshift(-1)
array.unshift(-2)

// Pop - Removes the value at the end of the array

array.pop()
array.pop()
array.pop()

// Shift - Removes the value at the start of an array
array.shift()
array.shift()
array.shift()

console.log(array)